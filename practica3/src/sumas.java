public class sumas {
    int suma;
    int positiva;
    int negativa;

    public sumas() {
        this.suma = 0;
        this.positiva = 0;
        this.negativa = 0;
    }

    public int getSuma() {
        return suma;
    }

    public void setSuma(int suma) {
        this.suma = suma;
    }

    public int getPositiva() {
        return positiva;
    }

    public void setPositiva(int positiva) {
        this.positiva = positiva;
    }

    public int getNegativa() {
        return negativa;
    }

    public void setNegativa(int negativa) {
        this.negativa = negativa;
    }

}
