public class sumAndDiff {
    private int sum;
    private int dif;

    public sumAndDiff() {
        sum = 0;
        dif = 0;
    }

    public sumAndDiff(int sum, int dif) {
        this.sum = sum;
        this.dif = dif;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }

    public void setDif(int dif) {
        this.dif = dif;
    }

    public int getSum() {
        return sum;
    }

    public int getDif() {
        return dif;
    }

}
